    private void btnDELETEbrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDELETEbrActionPerformed
        // TODO add your handling code here:
        try{
            updateAddDelAdmin();
            
            if(!(adname.isEmpty()) && !(adlname.isEmpty()) && addiploma.isEmpty() && adtype.isEmpty()){
                ps = sqlConn.prepareStatement("{call delete_admin(?,?)}");
                ps.setString(1, adname);
                ps.setString(2, adlname);
                int rowsAffected = ps.executeUpdate();
                if(rowsAffected == 1)
                    JOptionPane.showMessageDialog(this, "Record Deleted Successfully!");
                else
                    JOptionPane.showMessageDialog(this, "Record Not Deleted!");
            }else if(!(addiploma.isEmpty()) && adtype.isEmpty() && adname.isEmpty() && adlname.isEmpty()){
                ps = sqlConn.prepareStatement("DELETE FROM admin WHERE adm_AT=? AND (adm_diploma LIKE ?)");
                ps.setString(1, adadminAT);
                ps.setString(2, addiploma);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Record Deleted Successfully!");
            }else if(addiploma.isEmpty() && !(adtype.isEmpty()) && adname.isEmpty() && adlname.isEmpty()){
                ps = sqlConn.prepareStatement("DELETE FROM admin WHERE adm_AT=? AND (adm_type LIKE ?)");
                ps.setString(1, adadminAT);
                ps.setString(2, adtype);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Record Deleted Successfully!");
            }
        }catch(java.sql.SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnDELETEbrActionPerformed

