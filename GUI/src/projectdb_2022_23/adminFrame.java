    private void btnUPDATEbrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUPDATEbrActionPerformed
        // TODO add your handling code here:

        try{
            updateAddDelAdmin();
            
            if(!(addiploma.isEmpty()) && adtype.isEmpty()){
                ps = sqlConn.prepareStatement("UPDATE admin set adm_diploma=? WHERE adm_AT=?");
                ps.setString(2, adadminAT);
                ps.setString(1, addiploma);
                ps.executeUpdate();
            }else if(addiploma.isEmpty() && !(adtype.isEmpty())){
                ps = sqlConn.prepareStatement("UPDATE admin set adm_type=? WHERE adm_AT=?");
                ps.setString(2, adadminAT);
                ps.setString(1, adtype);
                ps.executeUpdate();
            }else{
                ps = sqlConn.prepareStatement("UPDATE admin set adm_type=?,adm_diploma=? WHERE adm_AT=?");
                ps.setString(1,adtype);
                ps.setString(2, addiploma);
                ps.setString(3, adadminAT);
                ps.executeUpdate();
            }
            

            adadminATtxt.setText("");
            addiplomatxt.setText("");
            adtypetxt.setText("");
            JOptionPane.showMessageDialog(this, "Record Updated Successfully!");
        }catch(Exception e){
            //java.util.logging.Logger.getLogger(branchFrame.class.getName()).log(java.util.logging.Level.SEVERE,null,e);
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnUPDATEbrActionPerformed

