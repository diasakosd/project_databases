    private void btnDELETEbrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDELETEbrActionPerformed
        // TODO add your handling code here:

        try{
            updateAddDelLanguages();
            if(lnglang.isEmpty()){
                ps = sqlConn.prepareStatement("DELETE FROM languages WHERE lng_gui_AT=?");
                ps.setString(1, lngguiAT);
                ps.executeUpdate();
            }else{
                ps = sqlConn.prepareStatement("DELETE FROM languages WHERE lng_gui_AT=? AND (lng_language LIKE ?)");
                ps.setString(1, lngguiAT);
                ps.setString(2, lnglang);
                ps.executeUpdate();
            }
            JOptionPane.showMessageDialog(this, "Record Deleted Successfully!");
        }catch(Exception e){
            //java.util.logging.Logger.getLogger(branchFrame.class.getName()).log(java.util.logging.Level.SEVERE,null,e);
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnDELETEbrActionPerformed

