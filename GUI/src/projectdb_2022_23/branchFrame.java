    private void btnUPDATEbrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUPDATEbrActionPerformed
        // TODO add your handling code here:
        try{
            updateAddDelBranch();
            
             if(!(brstreet.isEmpty()) && brnum.isEmpty() && brcity.isEmpty()){
                ps = sqlConn.prepareStatement("UPDATE branch set br_street=? WHERE br_code=?");
                ps.setString(2, brcode);
                ps.setString(1, brstreet);
                ps.executeUpdate();
            }else if(brstreet.isEmpty() && !(brnum.isEmpty()) && brcity.isEmpty()){
                ps = sqlConn.prepareStatement("UPDATE branch set br_num=? WHERE br_code=?");
                ps.setString(2, brcode);
                ps.setString(1, brnum);
                ps.executeUpdate();
            }else if(brstreet.isEmpty() && brnum.isEmpty() && !(brcity.isEmpty())){
                ps = sqlConn.prepareStatement("UPDATE branch set br_city=? WHERE br_code=?");
                ps.setString(2, brcode);
                ps.setString(1, brcity);
                ps.executeUpdate();
            }else{
                ps = sqlConn.prepareStatement("UPDATE branch set br_street=?,br_num=?,br_city=? WHERE br_code=?");
                ps.setString(1,brstreet);
                ps.setString(2, brnum);
                ps.setString(3, brcity);
                ps.setString(4, brcode);
                ps.executeUpdate();
            }
             
            JOptionPane.showMessageDialog(this, "Record Updated Successfully!");
        }catch(Exception e){
            //java.util.logging.Logger.getLogger(branchFrame.class.getName()).log(java.util.logging.Level.SEVERE,null,e);
            JOptionPane.showMessageDialog(null, e);
        }
    
    }//GEN-LAST:event_btnUPDATEbrActionPerformed

