    private void btnBRANCHINFOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBRANCHINFOActionPerformed
        // TODO add your handling code here:
        try{
            sqlConn = DriverManager.getConnection(dataB,username,password);
            CallableStatement cs = sqlConn.prepareCall("{call branch_info(?)}");
            cs.setString(1, brcodetxt.getText());
            ResultSet rs = cs.executeQuery();
            
            DefaultTableModel model = new DefaultTableModel();
            jTable1.setModel(model);
            ResultSetMetaData metaData = rs.getMetaData();
            int columnCount = metaData.getColumnCount();
            
            for (int i = 1; i <= columnCount; i++) {
                model.addColumn(metaData.getColumnLabel(i));
            }
            
            while (rs.next()) {
                Object[] rowData = new Object[columnCount];
                for (int i = 1; i <= columnCount; i++) {
                    rowData[i - 1] = rs.getObject(i);
                }
                model.addRow(rowData);
            }
            cs.close();
            sqlConn.close();
        }catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btnBRANCHINFOActionPerformed

